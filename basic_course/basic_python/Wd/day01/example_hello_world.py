def add_num(a, b):
    num1 = a
    num2 = b

    result = num1+num2

    return result

def minus_num(a, b):
    num1 = a
    num2 = b

    result = num1 - num2

    return result

def multiply_num(a, b):
    num1 = a
    num2 = b

    result = num1*num2

    return result

def divide_num(a, b):
    num1 = a
    num2 = b

    result = num1/num2

    return result

print(divide_num(4,2))